public class String_length {
    public static void main(String args[]) {
        String fName = "Ayush";
        String lName = "Devani";
        String fullName = fName + " " + lName;
        System.out.println("Name : " + fullName);
        System.out.println("Lenght of String = " + fullName.length());
    }
    
}
